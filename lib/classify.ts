export const DOMAIN_NAMES = Object.keys(KEYWORD_RULES);

/**
 * 根據文字內容自動分類領域（最多回傳 3 個）
 * @param fields - 可接受 name / company / product / description 任意組合
 */
export function classifyTeam(fields: {
  name?: string;
  description?: string;
  company?: string;
  product?: string;
}): string[] {
  const { name = "", description = "", company = "", product = "" } = fields;
  const text = `${name} ${company} ${product} ${description}`.toLowerCase();
  const scores: Record<string, number> = {};

  for (const [domain, keywords] of Object.entries(KEYWORD_RULES)) {
    let score = 0;
    for (const kw of keywords) {
      const kwLower = kw.toLowerCase();
      if (text.includes(kwLower)) {
        // description 權重 4, product 權重 3, 其他 2
        score += description.toLowerCase().includes(kwLower)
          ? 4
          : product.toLowerCase().includes(kwLower)
            ? 3
            : 2;
      }
    }
    if (score >= 3) scores[domain] = score;
  }

  return Object.entries(scores)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 3)
    .map(([d]) => d);
}

